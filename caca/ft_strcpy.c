#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	int i;

	i = 0;
	while (((const char *)src)[i])
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = (char)'\0';
	return (dst);
}
